Deno.serve(async (req) => {
    try {
        const base44 = createClientFromRequest(req);
        const user = await base44.auth.me();
        
        if (!user) {
            return Response.json({ error: 'Unauthorized' }, { status: 401 });
        }

        const body = await req.json();
        const { idea, platform, targetAudience, contentType, duration } = body;

        // Build comprehensive prompt
        const prompt = `אתה כותב סקריפטים ויראליים עבור עו"ד אוהד טבת. 

## מי אוהד טבת:
${CREATOR_PROFILE}

## הרעיון המקורי:
${idea}

## פרטי טכניים:
- פלטפורמה: ${platform}
- קהל יעד: ${targetAudience || 'עובדים שכירים שרוצים לדעת את הזכויות שלהם'}
- סוג תוכן: ${contentType || 'חינוכי'}
- אורך: ${duration || '60'} שניות

## הנחיות קריטיות:
${FRAMEWORK}

## בנק ההוקים להשראה:
${HOOKS_DATABASE}

## חוקי ברזל:
1. **הסקריפט חייב לזרום כמו שיחה טבעית** - לא רובוטי, לא מקוטע
2. אם ההוק מתחיל רשימה (כמו "3 סיבות ש...") - המשך עם הסיבות בצורה טבעית!
3. כתוב בסגנון של אוהד - ישיר, בגובה העיניים, עם דוגמאות מהחיים
4. אל תשתמש במילים מפוצצות מדי - תהיה אותנטי
5. הסקריפט המלא צריך להיות טקסט אחד זורם שאפשר לקרוא ולהקליט ישר
6. CTA קצר וטבעי - לא "תעקבו ותשתפו" אלא משהו שקשור לתוכן

## דוגמה לסקריפט טוב:
"למה העבודה שלי כאיש מכירות הפכה אותי לעורך דין הרבה יותר טוב?

סיבה ראשונה - אני מבין את הצורך של הלקוח שלי ברמה הכי עמוקה. כשאתה מגיע אליי, אני לא רק שומע מה אתה אומר, אני מבין מה אתה באמת צריך.

סיבה שנייה - אני יודע לזהות את סגנון התקשורת של כל אחד ולדבר בשפה שלו. זה עוזר גם מול הלקוחות וגם מול הצד השני במשא ומתן.

וסיבה שלישית - בבית הדין אני משלב טיעון משפטי עם טיעון מכירתי. אני לא רק מציג עובדות, אני משכנע. וזה שילוב מנצח.

אז אם יש לכם שאלה בנושא דיני עבודה - הגעתם למקום הנכון."

החזר JSON עם המבנה הבא:
{
  "hook": "ההוק - 3 שניות ראשונות מדויקות",
  "problem": "הבעיה - תיאור כאב הלקוח",
  "urgency": "דחיפות - למה עכשיו",
  "authority": "אוטוריטה - למה לסמוך עליך",
  "solution": "הפתרון - התובנה המרכזית",
  "application": "יישום - איך להפעיל",
  "cta": "קריאה לפעולה",
  "full_script": "הסקריפט המלא מחובר בצורה זורמת",
  "visual_notes": "הנחיות צילום: תנועות מצלמה, קאטים, טקסט על מסך",
  "hook_template": "שם התבנית שנבחרה מבנק ההוקים"
}`;

        const result = await base44.integrations.Core.InvokeLLM({
            prompt: prompt,
            response_json_schema: {
                type: "object",
                properties: {
                    hook: { type: "string", description: "משפט הפתיחה - 3 שניות ראשונות" },
                    main_points: { type: "string", description: "הנקודות העיקריות בסקריפט" },
                    cta: { type: "string", description: "קריאה לפעולה קצרה וטבעית" },
                    full_script: { type: "string", description: "הסקריפט המלא - טקסט אחד זורם וטבעי שאפשר להקליט ישר" },
                    visual_notes: { type: "string", description: "הנחיות צילום קצרות" },
                    hook_template: { type: "string", description: "סוג ההוק שנבחר" }
                }
            }
        });

        return Response.json({ 
            success: true,
            script: result
        });

    } catch (error) {
        console.error("Error generating script:", error);
        return Response.json({ error: error.message }, { status: 500 });
    }
});